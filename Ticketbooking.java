    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
// TODO add your handling code here:
try {
    String id = ID.getText();  // Assuming `ID` is the input field for the ID

    // Check if ID is entered
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter an ID to search.");
        return;
    }

    Class.forName("com.mysql.cj.jdbc.Driver");
    // Query to select details where the id matches the entered id
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ams", "root", "root")) {
        // Query to select details where the id matches the entered id
        String sql = "SELECT * FROM ticketbooking WHERE id = ?";
        PreparedStatement ptst = con.prepareStatement(sql);
        ptst.setString(1, id);
        
        ResultSet rs = ptst.executeQuery();
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);
        
        // Populate the table only if there is a matching record
        if (rs.next()) {
            Object o[] = { rs.getString("id"), rs.getString("name"), rs.getString("flightcode"),
                rs.getString("gender"), rs.getString("passportnumber"),
                rs.getString("amount"), rs.getString("nationality") };
            dt.addRow(o);
        } else {
            JOptionPane.showMessageDialog(this, "No record found with the entered ID.");
        }
    }
} catch (ClassNotFoundException | SQLException e) {
    JOptionPane.showMessageDialog(this, e);
}

    }//GEN-LAST:event_jButton3ActionPerformed

