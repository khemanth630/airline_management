    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      try {
    String id = ID.getText();
    String passengerName = pn.getText();
    String flightCode = fc.getText();
    String gender = g.getText();
    String passport = pass.getText();
    String amount = amt.getText();
    String nationality = n.getText();

    // Check if any field is empty
    if (id.isEmpty() || passengerName.isEmpty() || flightCode.isEmpty() || gender.isEmpty() || passport.isEmpty() || amount.isEmpty() || nationality.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields before saving.");
        return;  // Exit the method if any field is missing
    }

    Class.forName("com.mysql.cj.jdbc.Driver");
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ams", "root", "root")) {
        // Step 1: Check if the flight code exists in the manageflight table
        String flightCheckSQL = "SELECT COUNT(*) FROM manageflight WHERE flightcode = ?";
        PreparedStatement flightCheckStmt = con.prepareStatement(flightCheckSQL);
        flightCheckStmt.setString(1, flightCode);
        
        ResultSet flightCheckRs = flightCheckStmt.executeQuery();
        if (flightCheckRs.next() && flightCheckRs.getInt(1) > 0) {
            // Step 2: If the flight code exists, proceed with the insert
            String sql = "INSERT INTO ticketbooking VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ptst = con.prepareStatement(sql);
            ptst.setString(1, id);
            ptst.setString(2, passengerName);
            ptst.setString(3, flightCode);
            ptst.setString(4, gender);
            ptst.setString(5, passport);
            ptst.setString(6, amount);
            ptst.setString(7, nationality);

            try {
                ptst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data inserted successfully!");
            } catch (SQLException e) {
                // Catch and display trigger error if it occurs
                JOptionPane.showMessageDialog(this, e.getMessage(), "Trigger Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Step 3: If the flight code does not exist, show an error message
            JOptionPane.showMessageDialog(this, "Please enter a valid flight code.");
        }
    }
} catch (ClassNotFoundException | SQLException e) {
    JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Connection Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_jButton1ActionPerformed

